DROP TABLE IF EXISTS tags;
DROP TABLE IF EXISTS listings;
DROP TABLE IF EXISTS tag_listings;
DROP TABLE IF EXISTS hub_links;
DROP TABLE IF EXISTS hub_tags;



CREATE TABLE tags (
  id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  name TEXT UNIQUE
);

CREATE TABLE listings (
  id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  name TEXT NOT NULL,
  url TEXT NOT NULL UNIQUE,
  description TEXT
);

CREATE TABLE tag_listings (
  listing_id INTEGER,
  tag_id INTEGER
);

CREATE TABLE hub_tags (
  hub_id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  tag_id INTEGER
);

CREATE TABLE hub_links (
  hub_id INTEGER,
  sub_hub INTEGER
);